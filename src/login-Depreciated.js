import React, { Fragment, useState } from 'react'
import Head from 'next/head'
import Image from 'next/image'
import { Inter } from 'next/font/google'
import styles from '@/styles/Home.module.css'
import Navbar from '@/src/navbar'
import registerImage from '@/assets/images/jacob-padilla-sqHBRY7sF1o-unsplash.jpg'
import loginImage from '@/assets/images/juan-domenech-zFLCMUTN9ek-unsplash.jpg'
import Footer from '@/src/footer'
const inter = Inter({ subsets: ['latin'] })

const Login = () => {
    const [headVar , setHeadVar] = useState('Login');
    const [loginVar , setLoginVar] = useState('flex');
    const [regisVar , setRegisVar] = useState('none');
    const handleSwitch = (e)=>{
        if(headVar == 'Login'){
            setHeadVar('Register');
            setLoginVar('none');
            setRegisVar('flex');
        }else{
            setHeadVar('Login');
            setLoginVar('flex');
            setRegisVar('none');
        }
    }
    const containerStyle = {
        display:'flex',
        flexDirection:'row',
        height:'350px',
        gap:'0px',
    }
    const frameStyle = {
        width:'50%',
        height:'100%',
    }
  return (
    <Fragment>
        <Head>
            <title>Login</title>
            <meta name="description" content="Generated by create next app" />
            <meta name="viewport" content="width=device-width, initial-scale=1" />
            <link rel="icon" href="/favicon.ico" />
        </Head>
        <Navbar/>
        <main className={` ${inter.className}`}>
            <div className={styles.center} style={{display:'flex' , flexDirection:'column' , gap:'20px'}}>
                <div >
                    <h1 style={{fontSize:"3em"}}>{headVar}</h1>
                </div>
                <div className={`signContainer`} style={containerStyle}>
                    <div style={{...frameStyle , backgroundColor:'red' , display:loginVar}}><Image src={loginImage} style={{width:"100%",height:"100%"}}/></div>
                    <div style={{...frameStyle , backgroundColor:'blue',display:loginVar}}>
                        <form>
                            <input type='text'placeholder='Username'/>
                            <input type='password'placeholder='Password'/>
                            <button type='Submit'>Login</button>
                            <p>Don't Have an Account ? <a onClick={handleSwitch}>Register</a> .</p>
                        </form>
                    </div>
                    <div style={{...frameStyle , backgroundColor:'purple',display:regisVar}}>
                        <form>
                            <div>
                                <input type='text'placeholder='First Name' name='fname'/>
                                <input type='text'placeholder='Last Name' name='lname'/>
                            </div>
                            <input type='text'placeholder='E-mail' name='email'/>
                            <input type='text'placeholder='Username' name='username'/>
                            <input type='password'placeholder='Password' name='password'/>
                            <button type='Submit'>Login</button>
                            <p>Already Have an Account ? <a onClick={handleSwitch}>Login</a> .</p>
                        </form>
                    </div>
                    <div style={{...frameStyle , backgroundColor:'black' , display:regisVar}}><Image src={registerImage} style={{width:"100%",height:"100%"}}/></div>
                </div>
            </div>
        </main>
    <Footer/>
    </Fragment>
  )
}

export default Login